using System;
using Bitcoin.Api.Extensions;

namespace Bitcoin.Api.Response
{
    ////////////////////////////////////////////////////////////////////////////////////////////////////
    /// <summary>   A converted bitcoin point. </summary>
    ///
    /// <remarks>   Maxim, 1/19/2017. </remarks>
    ////////////////////////////////////////////////////////////////////////////////////////////////////

    public class ConvertedBitcoinPoint
    {
        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Constructor. </summary>
        ///
        /// <remarks>   Maxim, 1/19/2017. </remarks>
        ///
        /// <param name="point">    The point. </param>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public ConvertedBitcoinPoint(RawBitcoinPoint point)
        {
            CaptureDate = point.X.ToUnixTimeStamp();
            Value = point.Y;
        }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets the capture date. Converted from unix time stamp returned as X</summary>
        ///
        /// <value> The capture date. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public DateTime CaptureDate { get; }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets the value returned as Y </summary>
        ///
        /// <value> The value. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public double Value { get; }
    }
}