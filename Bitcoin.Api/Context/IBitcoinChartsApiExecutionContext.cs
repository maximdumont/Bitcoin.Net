    /// <remarks>   Maxim, 1/19/2017. </remarks>
    ///
    /// <typeparam name="T">    Generic type parameter. </typeparam>
    ////////////////////////////////////////////////////////////////////////////////////////////////////

    public interface IBitcoinChartsApiExecutionContext<out T> where T : new()
    {
        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Executes the given request. </summary>
        ///
        /// <param name="request">  The request. </param>
        ///
        /// <returns>   A T. </returns>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        T Execute(IRestRequest request);

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Executes the given request</summary>
        ///
        /// <param name="request">  The request. </param>
        ///
        /// <returns>   A T. </returns>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        T Execute(IBitcoinChartsApiRequest request);
    }
}